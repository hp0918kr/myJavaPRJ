package src.main.java;

public class Quiz0329 {
    public static void main(String[] args) {
        for (int k = 1; k < 10; k++) {
            for (int i = 2; i < 10; i++) {
                System.out.print(i + "x" + k + "=" + i * k + " ");
            }
            System.out.println();
        }
    }
}
